package modelo;

public class Location {
       private String locationName;
       private String description;
       private String phoneNumber;
       private String deliveryStoreTime;
       private String receptionTime;
       private String presentialTime;
       private String locationImage;

    public String getLocationName() {
        return locationName;
    }

    public void setLocationName(String locationName) {
        this.locationName = locationName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public String getDeliveryStoreTime() {
        return deliveryStoreTime;
    }

    public void setDeliveryStoreTime(String deliveryStoreTime) {
        this.deliveryStoreTime = deliveryStoreTime;
    }

    public String getReceptionTime() {
        return receptionTime;
    }

    public void setReceptionTime(String receptionTime) {
        this.receptionTime = receptionTime;
    }

    public String getPresentialTime() {
        return presentialTime;
    }

    public void setPresentialTime(String presentialTime) {
        this.presentialTime = presentialTime;
    }

    public String getLocationImage() {
        return locationImage;
    }

    public void setLocationImage(String locationImage) {
        this.locationImage = locationImage;
    }
       
       
}
