
package modelo;

public class ClaseProducto {
    
    int cant;
    double precio;
    String produ;

    public int getCant() {
        return cant;
    }

    public void setCant(int cant) {
        this.cant = cant;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public String getProdu() {
        return produ;
    }

    public void setProdu(String produ) {
        this.produ = produ;
    }
}

